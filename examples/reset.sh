#!/usr/bin/env bash
# Откат прогона нормализатора к состоянию из git: восстанавливает отслеживаемые
# файлы и удаляет неотслеживаемые объекты (например, опустевшие нормализованные
# каталоги). Выполняется без запроса.
#
# Безопасность (см. также раздел в README):
#  - работает ТОЛЬКО внутри каталога самого скрипта (.), никогда не трогает
#    файлы вне него.
#
# Вся логика обёрнута в функцию main и вызывается последней строкой: так
# интерпретатор читает скрипт целиком ДО выполнения и корректно отрабатывает,
# даже если git-операции перепишут сам файл на диске.
set -euo pipefail

main() {
    local here
    here="$(cd -- "$(dirname -- "$0")" && pwd)"
    cd "$here"

    if ! git rev-parse --is-inside-work-tree >/dev/null 2>&1; then
        echo "Это не git-репозиторий — откат через git недоступен." >&2
        exit 1
    fi

    # 1. Возвращаем отслеживаемые файлы к версии из git (только в этом каталоге).
    if ! git restore --staged --worktree -- . 2>/dev/null; then
        # Откат для старого git (< 2.23), где нет команды restore.
        git reset -q -- .
        git checkout -- .
    fi

    # 2. Удаляем неотслеживаемые объекты (опустевшие нормализованные каталоги и пр.).
    git clean -fd -- .

    echo "Готово: examples/ возвращён к состоянию из git." >&2
}

main "$@"
