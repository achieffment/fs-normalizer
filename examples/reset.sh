#!/usr/bin/env bash
# Откат прогона нормализатора к состоянию из git.
#
# Безопасность (см. также раздел в README):
#  - работает ТОЛЬКО внутри каталога самого скрипта (.), никогда не трогает
#    файлы вне него;
#  - отслеживаемые файлы лишь возвращаются к версии из git (содержимое из
#    коммита всегда восстановимо); сами reset-скрипты при этом не трогаются;
#  - неотслеживаемые объекты (их git восстановить не может) удаляются ТОЛЬКО
#    после явного подтверждения и показа полного списка.
#
# Вся логика обёрнута в функцию main и вызывается последней строкой: так
# интерпретатор читает скрипт целиком ДО выполнения и не может «дочитать»
# изменённую версию, если git-операции затронут файл на диске.
set -euo pipefail

main() {
    local here ans leftovers
    here="$(cd -- "$(dirname -- "$0")" && pwd)"
    cd "$here"

    if ! git rev-parse --is-inside-work-tree >/dev/null 2>&1; then
        echo "Это не git-репозиторий — откат через git недоступен." >&2
        exit 1
    fi

    # Сами reset-скрипты исключаем из всех операций (и из восстановления, и из
    # удаления), чтобы скрипт не изменил и не удалил сам себя.
    local keep=(':(exclude)reset.sh' ':(exclude)reset.command' ':(exclude)reset.bat')
    local excl=(-e reset.sh -e reset.command -e reset.bat)

    # 1. Возвращаем ОТСЛЕЖИВАЕМЫЕ файлы к версии из git (только в этом каталоге).
    if ! git restore --staged --worktree -- . "${keep[@]}" 2>/dev/null; then
        # Откат для старого git (< 2.23), где нет команды restore.
        git reset -q -- . "${keep[@]}"
        git checkout -- . "${keep[@]}"
    fi

    # 2. Неотслеживаемые объекты (например, опустевшие нормализованные каталоги).
    leftovers="$(git clean -nd "${excl[@]}" -- . | sed 's/^Would remove //')"
    if [ -z "$leftovers" ]; then
        echo "Готово: дерево уже соответствует git, удалять нечего." >&2
        exit 0
    fi

    echo "Будут удалены неотслеживаемые объекты (их НЕТ в git, восстановить нельзя):" >&2
    printf '%s\n' "$leftovers" | sed 's/^/  /' >&2
    printf "Удалить перечисленное? [y/N]: " >&2
    read -r ans || ans=""
    case "$ans" in
        [yYдД]*)
            git clean -fd "${excl[@]}" -- .
            echo "Готово: дерево возвращено к состоянию из git." >&2
            ;;
        *)
            echo "Отменено: неотслеживаемые объекты сохранены (отслеживаемые уже восстановлены)." >&2
            ;;
    esac
}

main "$@"
